# Define a function to calculate the square of a number
def square(num):
    return num * num

# Define a function to greet a user
def greet(name):
    print(f"Hello, {name}! Welcome to Python.")

# Define a function to check if a number is even
def is_even(number):
    return number % 2 == 0

# Using the functions
print(square(5))        
greet("FARHAN")           
print(is_even(10))      
print(is_even(7))        